package complexPojo_02;

public class Currency {

	private String name;
	private String symbol;
	
	public Currency(String name, String symbol) {
		
		this.name = name;
		this.symbol = symbol;
	}
	
	public Currency() {
		
	}

	public String getName() {
		return name;
	}

	public String getSymbol() {
		return symbol;
	}
	
	
	
	
}
