package Pojo;

public class Propertiez {

	private String age;
	private String salary;

	public Propertiez(String age, String salary) {

		this.age = age;
		this.salary = salary;
	}

	public Propertiez() {

	}

	public String getAge() {
		return age;
	}

	public String getSalary() {
		return salary;
	}
	
	

}
